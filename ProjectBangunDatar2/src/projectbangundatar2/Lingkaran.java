public class Lingkaran {
    
    //method untuk menghitung luas lingkaran
    double hitungLuas(double jariJari){
        return (3.14285 * jariJari * jariJari);
        
    }
    
    double hitungKeliling(double jariJari){
        return (2 * 3.14285 * jariJari);
    }
}
